# Regex: сначала длинные паттерны, потом короткие
# Исключаем regex-паттерны с \b — они обрабатываются отдельно
_SIMPLE_KEYS = {k: v for k, v in _BERTISLAV_MAP.items() if "\\" not in k and len(k) > 0}
_BERTISLAV_RE = re.compile(
    "|".join(re.escape(k) for k in sorted(_SIMPLE_KEYS, key=len, reverse=True))
)


def norm_bertislav(text: str) -> str:
    """Один regex-проход вместо ~6900 вызовов re.sub."""
    text = text.lower()

    # Граничные паттерны — обрабатываем отдельно (их мало)
    text = re.sub(r"\bѿ\b", "отъ", text)
    text = re.sub(r"\bоц",  "отц", text)
    text = re.sub(r"\bєс\b", "естъ", text)
    text = re.sub(r"\bѥс\b", "ѥстъ", text)
    text = re.sub(r"[0-9]+[vr]", " ", text)
    text = re.sub(r"\.{2,}", "", text)

    # Один проход для всех остальных замен
    text = _BERTISLAV_RE.sub(lambda m: _SIMPLE_KEYS[m.group(0)], text)

    # Финальная нормализация пробелов
    text = re.sub(r"\t+", "\t", text)
    text = re.sub(r" +",  " ",  text)
    text = re.sub(r"\n+", "\n", text)
    text = re.sub(r"\s\.", ".", text)
    text = re.sub(r"\s,",  ",", text)
    # text = re.sub(r"\s·",  "·", text)

    return text.strip()


