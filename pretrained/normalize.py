# Разделяем на 1:1 (для translate) и многосимвольные (для regex)
_MBERT_1TO1  = {ord(k): v for k, v in MBERT_NORMALIZATION_MAP.items()
                if len(k) == 1 and len(v) <= 1}
_MBERT_MULTI = {k: v for k, v in MBERT_NORMALIZATION_MAP.items()
                if len(k) > 1 or len(v) > 1}
_MBERT_TABLE = str.maketrans(_MBERT_1TO1)

if _MBERT_MULTI:
    _MBERT_RE = re.compile(
        "|".join(re.escape(k) for k in sorted(_MBERT_MULTI, key=len, reverse=True))
    )
else:
    _MBERT_RE = None


def norm_mbert(text: str) -> str:
    # Многосимвольные замены (ѯ→кс, ѱ→пс, ѿ→от, ѹ→оу, ꙋ→у)
    if _MBERT_RE:
        text = _MBERT_RE.sub(lambda m: _MBERT_MULTI[m.group(0)], text)
    # Односимвольные — translate() работает за O(n)
    return text.translate(_MBERT_TABLE)
    